public class Trapecio {
    private int bMayor;
    private int bMenor;

    public Trapecio(int bMayor, int bMenor){
        this.bMayor = bMayor;
        this.bMenor = bMenor;
    }
    
    public int getBMayor(){
        
        return bMayor;
    }

    public int getBmenor(){
        return bMenor;
    }

}
